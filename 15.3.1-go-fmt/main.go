package main;import "fmt"

/**
 * -------------------------------------------------------
 * 15.3.1 ソースコードの整形 (go fmtコマンド)
 * -------------------------------------------------------
 * ※ ソースコードを整形する「go fmt」コマンドが標準で用意されています
 * ※ 利用方法：
 *    // カレントディレクトリのファイルを整形する場合
 *    $ go fmt
 *
 *    // パッケージのファイルを整形する場合
 *    $ go fmt パッケージ名
 *
 *    // 1つのファイルを整形する場合
 *    $ go fmt ファイルのパス
 */
func main() {
fmt.Println("hello")
}
